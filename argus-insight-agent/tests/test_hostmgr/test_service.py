# ---------------------------------------------------------------------------
# Hostname tests
# ---------------------------------------------------------------------------


async def test_get_hostname():
    with patch.object(service, "_run", new_callable=AsyncMock) as mock_run:
        mock_run.side_effect = [
            (0, "myhost", ""),
            (0, "myhost.example.com", ""),
        ]
        result = await service.get_hostname()

    assert result.hostname == "myhost"
    assert result.fqdn == "myhost.example.com"


async def test_get_hostname_fqdn_empty_falls_back():
    with patch.object(service, "_run", new_callable=AsyncMock) as mock_run:
        mock_run.side_effect = [
            (0, "myhost", ""),
            (0, "", "hostname: Name or service not known"),
        ]
        result = await service.get_hostname()

    assert result.hostname == "myhost"
    assert result.fqdn == "myhost"


async def test_change_hostname_success():
    with patch.object(service, "_run", new_callable=AsyncMock) as mock_run:
        mock_run.return_value = (0, "", "")
        result = await service.change_hostname("newhost")

    assert result.success is True
    assert "newhost" in result.message


async def test_change_hostname_failure():
    with patch.object(service, "_run", new_callable=AsyncMock) as mock_run:
        mock_run.return_value = (1, "", "Could not set hostname")
        result = await service.change_hostname("bad-host")

    assert result.success is False
    assert "Failed" in result.message


async def test_validate_hostname_consistent():
    with patch.object(service, "_run", new_callable=AsyncMock) as mock_run:
        mock_run.side_effect = [
            (0, "myhost", ""),
            (0, "myhost.example.com", ""),
        ]
        result = await service.validate_hostname()

    assert result.is_consistent is True
    assert result.hostname == "myhost"
    assert result.fqdn == "myhost.example.com"


async def test_validate_hostname_inconsistent():
    with patch.object(service, "_run", new_callable=AsyncMock) as mock_run:
        mock_run.side_effect = [
            (0, "myhost", ""),
            (0, "otherhost.example.com", ""),
        ]
        result = await service.validate_hostname()

    assert result.is_consistent is False
    assert "mismatch" in result.message.lower()


