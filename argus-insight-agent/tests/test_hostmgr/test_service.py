def test_get_nameservers(resolv_file):
    with patch.object(service, "RESOLV_CONF", resolv_file):
        result = service.get_nameservers()

    assert len(result.nameservers) == 2
    assert result.nameservers[0].address == "8.8.8.8"
    assert result.nameservers[1].address == "8.8.4.4"
    assert "example.com" in result.search_domains
    assert "edns0" in result.options


def test_get_nameservers_missing_file(tmp_path):
    missing = tmp_path / "nonexistent"
    with patch.object(service, "RESOLV_CONF", missing):
        result = service.get_nameservers()

    assert result.nameservers == []
    assert result.raw == ""


def test_update_nameservers(resolv_file):
    with patch.object(service, "RESOLV_CONF", resolv_file):
        result = service.update_nameservers(["1.1.1.1", "1.0.0.1"])

    assert result.success is True
    content = resolv_file.read_text()
    assert "nameserver 1.1.1.1" in content
    assert "nameserver 1.0.0.1" in content
    # Old nameservers should be gone
    assert "nameserver 8.8.8.8" not in content
    # search/options should be preserved
    assert "search example.com" in content
    assert "options edns0 trust-ad" in content


def test_update_nameservers_empty_list(resolv_file):
    with patch.object(service, "RESOLV_CONF", resolv_file):
        result = service.update_nameservers([])

    assert result.success is False
    assert "required" in result.message.lower()


def test_update_nameservers_creates_file(tmp_path):
    missing = tmp_path / "resolv.conf"
    with patch.object(service, "RESOLV_CONF", missing):
        result = service.update_nameservers(["1.1.1.1"])

    assert result.success is True
    assert "nameserver 1.1.1.1" in missing.read_text()
