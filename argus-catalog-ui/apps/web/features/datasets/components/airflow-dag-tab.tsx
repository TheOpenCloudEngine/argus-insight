function generateBasicDag(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, connId, hdfsBase, columns, dagId } = getDatasetParts(dataset)

  return `"""
Airflow DAG: Basic JDBC Extract → HDFS Load
Source: ${dataset.platform.name} (${platformId})
Table: ${dbName}.${tblName}
Generated by Argus Catalog
"""

from airflow import DAG
from airflow.providers.common.sql.operators.sql import SQLExecuteQueryOperator
from airflow.operators.python import PythonOperator
from datetime import datetime, timedelta
import subprocess
import tempfile
import csv
import os

default_args = {
    "owner": "argus-catalog",
    "depends_on_past": False,
    "retries": 2,
    "retry_delay": timedelta(minutes=5),
    "email_on_failure": False,
}


def _upload_to_hdfs(**context):
    """Upload extracted data to HDFS."""
    ti = context["ti"]
    rows = ti.xcom_pull(task_ids="extract")

    # Write to temp CSV
    tmp = tempfile.NamedTemporaryFile(mode="w", suffix=".csv", delete=False)
    writer = csv.writer(tmp)
    writer.writerow([${columns.split(", ").map(c => `"${c}"`).join(", ")}])
    for row in rows:
        writer.writerow(row)
    tmp.close()

    # Upload to HDFS
    hdfs_path = "${hdfsBase}/data.csv"
    subprocess.run(["hdfs", "dfs", "-mkdir", "-p", "${hdfsBase}"], check=True)
    subprocess.run(["hdfs", "dfs", "-put", "-f", tmp.name, hdfs_path], check=True)
    os.unlink(tmp.name)
    print(f"Uploaded {len(rows)} rows to {hdfs_path}")


with DAG(
    dag_id="${dagId}",
    default_args=default_args,
    description="Ingest ${dbName}.${tblName} from ${platformType} to HDFS",
    schedule="0 2 * * *",
    start_date=datetime(2026, 1, 1),
    catchup=False,
    tags=["argus-catalog", "${platformType}", "${dbName}"],
) as dag:

    extract = SQLExecuteQueryOperator(
        task_id="extract",
        conn_id="${connId}",
        sql="SELECT ${columns} FROM ${dbName}.${tblName}",
        do_xcom_push=True,
    )

    upload = PythonOperator(
        task_id="upload_to_hdfs",
        python_callable=_upload_to_hdfs,
    )

    extract >> upload
`
}

function generateSparkDag(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, jdbc, hdfsBase, dagId, pkColumn } = getDatasetParts(dataset)

  return `"""
Airflow DAG: PySpark Partitioned Load
Source: ${dataset.platform.name} (${platformId})
Table: ${dbName}.${tblName}
Generated by Argus Catalog
"""

from airflow import DAG
from airflow.providers.apache.spark.operators.spark_submit import SparkSubmitOperator
from airflow.operators.python import PythonOperator
from datetime import datetime, timedelta
import tempfile
import os

default_args = {
    "owner": "argus-catalog",
    "depends_on_past": False,
    "retries": 2,
    "retry_delay": timedelta(minutes=5),
}

PYSPARK_SCRIPT = """
from pyspark.sql import SparkSession
from pyspark.sql.functions import lit
import sys

target_date = sys.argv[1]

spark = SparkSession.builder \\\\
    .appName("airflow-${platformId}-${tblName}") \\\\
    .getOrCreate()

df = spark.read \\\\
    .format("jdbc") \\\\
    .option("url", "${jdbc.url}") \\\\
    .option("driver", "${jdbc.driver}") \\\\
    .option("dbtable", f"(SELECT * FROM ${dbName}.${tblName} WHERE DATE(last_update) = '{target_date}') AS t") \\\\
    .option("user", "<USERNAME>") \\\\
    .option("password", "<PASSWORD>") \\\\
    .load()

print(f"Read {df.count()} rows for {target_date}")

df.withColumn("dt", lit(target_date)) \\\\
    .write \\\\
    .mode("append") \\\\
    .partitionBy("dt") \\\\
    .parquet("${hdfsBase}")

print(f"Written to ${hdfsBase}/dt={target_date}")
spark.stop()
"""


def _write_spark_script(**context):
    """Write PySpark script to a temp file for SparkSubmitOperator."""
    f = tempfile.NamedTemporaryFile(mode="w", suffix=".py", delete=False, dir="/tmp")
    f.write(PYSPARK_SCRIPT)
    f.close()
    context["ti"].xcom_push(key="script_path", value=f.name)


def _cleanup_script(**context):
    path = context["ti"].xcom_pull(task_ids="write_script", key="script_path")
    if path and os.path.exists(path):
        os.unlink(path)


with DAG(
    dag_id="${dagId}_spark",
    default_args=default_args,
    description="PySpark partitioned ingest ${dbName}.${tblName}",
    schedule="0 3 * * *",
    start_date=datetime(2026, 1, 1),
    catchup=False,
    tags=["argus-catalog", "${platformType}", "${dbName}", "spark"],
) as dag:

    write_script = PythonOperator(
        task_id="write_script",
        python_callable=_write_spark_script,
    )

    spark_submit = SparkSubmitOperator(
        task_id="spark_partitioned_load",
        application="{{ ti.xcom_pull(task_ids='write_script', key='script_path') }}",
        conn_id="spark_default",
        application_args=["{{ ds }}"],
        conf={
            "spark.executor.memory": "2g",
            "spark.executor.cores": "2",
            "spark.dynamicAllocation.enabled": "true",
        },
    )

    cleanup = PythonOperator(
        task_id="cleanup",
        python_callable=_cleanup_script,
        trigger_rule="all_done",
    )

    write_script >> spark_submit >> cleanup
`
}

function generateIncrementalDag(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, connId, hdfsBase, columns, dagId } = getDatasetParts(dataset)

  return `"""
Airflow DAG: Incremental Date-based Load
Source: ${dataset.platform.name} (${platformId})
Table: ${dbName}.${tblName}
Generated by Argus Catalog

Uses Airflow's Jinja templating ({{ ds }}) for date-based extraction.
The '{{ ds }}' macro resolves to the logical execution date (YYYY-MM-DD).
"""

from airflow import DAG
from airflow.providers.common.sql.operators.sql import SQLExecuteQueryOperator
from airflow.operators.bash import BashOperator
from airflow.operators.python import PythonOperator
from datetime import datetime, timedelta
import tempfile
import csv
import os

default_args = {
    "owner": "argus-catalog",
    "depends_on_past": True,  # Ensure sequential date processing
    "retries": 3,
    "retry_delay": timedelta(minutes=10),
}

HDFS_BASE = "${hdfsBase}"


def _save_and_upload(**context):
    """Save query results to CSV and upload to HDFS partition."""
    ti = context["ti"]
    ds = context["ds"]  # Execution date: YYYY-MM-DD
    rows = ti.xcom_pull(task_ids="extract_daily")

    if not rows:
        print(f"No data for {ds}, skipping upload.")
        return

    # Write temp file
    tmp = tempfile.NamedTemporaryFile(mode="w", suffix=".csv", delete=False)
    writer = csv.writer(tmp)
    writer.writerow([${columns.split(", ").map(c => `"${c}"`).join(", ")}])
    for row in rows:
        writer.writerow(row)
    tmp.close()

    # Upload to HDFS partition
    partition_path = f"{HDFS_BASE}/dt={ds}"
    os.system(f"hdfs dfs -mkdir -p {partition_path}")
    os.system(f"hdfs dfs -put -f {tmp.name} {partition_path}/${tblName}.csv")
    os.unlink(tmp.name)
    print(f"Uploaded {len(rows)} rows to {partition_path}")


with DAG(
    dag_id="${dagId}_incremental",
    default_args=default_args,
    description="Incremental daily ingest ${dbName}.${tblName}",
    schedule="0 2 * * *",
    start_date=datetime(2026, 1, 1),
    catchup=False,
    tags=["argus-catalog", "${platformType}", "${dbName}", "incremental"],
) as dag:

    # Extract only rows matching the execution date
    # {{ ds }} is Airflow's Jinja template for execution date (YYYY-MM-DD)
    extract_daily = SQLExecuteQueryOperator(
        task_id="extract_daily",
        conn_id="${connId}",
        sql=\"\"\"
            SELECT ${columns}
            FROM ${dbName}.${tblName}
            WHERE DATE(last_update) = '{{ ds }}'
        \"\"\",
        do_xcom_push=True,
    )

    upload = PythonOperator(
        task_id="upload_to_hdfs",
        python_callable=_save_and_upload,
    )

    verify = BashOperator(
        task_id="verify_partition",
        bash_command="hdfs dfs -ls ${hdfsBase}/dt={{ ds }}/ && echo 'Partition verified'",
    )

    extract_daily >> upload >> verify
`
}

function generateSqlDag(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, hdfsBase, dagId } = getDatasetParts(dataset)
  const fields = dataset.schema_fields

  return `"""
Airflow DAG: Hive/Trino SQL Pipeline
Source: ${dataset.platform.name} (${platformId})
Table: ${dbName}.${tblName}
Generated by Argus Catalog

Uses Trino/Hive SQL operators to create external tables,
manage partitions, and load data via INSERT INTO.
"""

from airflow import DAG
from airflow.providers.common.sql.operators.sql import SQLExecuteQueryOperator
from airflow.operators.bash import BashOperator
from datetime import datetime, timedelta

default_args = {
    "owner": "argus-catalog",
    "depends_on_past": False,
    "retries": 2,
    "retry_delay": timedelta(minutes=5),
}

TRINO_CONN_ID = "trino_default"
CATALOG_SCHEMA = "catalog_${platformId.replace(/-/g, "_")}"

with DAG(
    dag_id="${dagId}_sql",
    default_args=default_args,
    description="SQL-based partitioned ingest ${dbName}.${tblName}",
    schedule="0 4 * * *",
    start_date=datetime(2026, 1, 1),
    catchup=False,
    tags=["argus-catalog", "${platformType}", "${dbName}", "sql", "trino"],
) as dag:

    # Step 1: Create external table (run once, then disable)
    create_table = SQLExecuteQueryOperator(
        task_id="create_external_table",
        conn_id=TRINO_CONN_ID,
        sql=\"\"\"
            CREATE TABLE IF NOT EXISTS {schema}.${tblName} (
                ${fields.map(f => `${f.field_path} VARCHAR`).join(",\\n                ")}
            )
            WITH (
                format = 'PARQUET',
                partitioned_by = ARRAY['dt'],
                external_location = '${hdfsBase}'
            )
        \"\"\".format(schema=CATALOG_SCHEMA),
    )

    # Step 2: Insert data for the execution date
    insert_data = SQLExecuteQueryOperator(
        task_id="insert_daily_data",
        conn_id=TRINO_CONN_ID,
        sql=\"\"\"
            INSERT INTO {schema}.${tblName}
            SELECT
                ${fields.map(f => `CAST(${f.field_path} AS VARCHAR)`).join(",\\n                ")},
                '{{{{ ds }}}}' AS dt
            FROM ${platformType}.${dbName}.${tblName}
            WHERE DATE(last_update) = DATE '{{{{ ds }}}}'
        \"\"\".format(schema=CATALOG_SCHEMA),
    )

    # Step 3: Sync partition metadata
    sync_partitions = SQLExecuteQueryOperator(
        task_id="sync_partitions",
        conn_id=TRINO_CONN_ID,
        sql=\"\"\"
            CALL system.sync_partition_metadata(
                schema_name => '{schema}',
                table_name => '${tblName}',
                mode => 'ADD'
            )
        \"\"\".format(schema=CATALOG_SCHEMA),
    )

    # Step 4: Verify row count
    verify = SQLExecuteQueryOperator(
        task_id="verify_count",
        conn_id=TRINO_CONN_ID,
        sql=\"\"\"
            SELECT count(*) AS row_count
            FROM {schema}.${tblName}
            WHERE dt = '{{{{ ds }}}}'
        \"\"\".format(schema=CATALOG_SCHEMA),
        do_xcom_push=True,
    )

    create_table >> insert_data >> sync_partitions >> verify
`
}

